// 随机开头方式
const OPENINGS = [
  "用一个引人好奇的问题开头",
  "用一个反转/惊喜的结论开头",
  "用一段简短的个人经历开头",
  "用吐槽/抱怨开头（制造共鸣）",
  "直接甩结论，然后再解释",
  "用场景描述开头（时间、地点、心情）",
  "用一张对比（before/after）开头",
  "用一个冷知识或反常识观点开头",
]

// 随机结构模板
const STRUCTURES = [
  "体验过程 → 真实感受 → 总结推荐",
  "提出问题 → 踩坑经历 → 找到答案 → 分享",
  "列出 3-5 个要点式分享（非列表格式，而是口语化分段）",
  "对比之前 vs 之后，层层递进",
  "从一个细节切入，慢慢展开到整体",
  "先抑后扬：先说不满意的地方，再说为什么最后还是爱了",
  "提问互动式：中间穿插'你们有没有这种感觉'之类的互动",
  "日记体：按时间线叙述（早上→中午→晚上 的体验变化）",
]

// 标题风格随机
const TITLE_STYLES = [
  "一个感叹句 + 一个悬念标题 + 一个直给结论标题",
  "一个数字型标题 + 一个情绪型标题 + 一个问句式标题",
  "一个对比型标题 + 一个口语化标题 + 一个夸张式标题",
  "一个痛点型标题 + 一个好奇型标题 + 一个简洁有力型标题",
]

// 随机个人细节
const PERSONAL_DETAILS = [
  "提到一个具体的失败/踩坑经历",
  "提到一个具体的时间点（比如上周二晚上）",
  "提到一个具体的数字（比如第三次回购）",
  "提到一个朋友的推荐或评价",
  "提到自己的肤质/口味/习惯等个人特征",
  "提到一个地点（比如某个商场、某个城市）",
  "提到当下的情绪变化（从怀疑到惊喜的过程）",
]

function pick<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)]
}

function pickN<T>(arr: T[], n: number): T[] {
  const shuffled = [...arr].sort(() => Math.random() - 0.5)
  return shuffled.slice(0, n)
}

export function buildSystemPrompt(): string {
  const opening = pick(OPENINGS)
  const structure = pick(STRUCTURES)
  const titleStyle = pick(TITLE_STYLES)
  const details = pickN(PERSONAL_DETAILS, 2)

  return `你是一个小红书文案专家。你的每一篇笔记都要有独特的风格，绝对不能用固定模板。

## 本次创作要求

**开头方式：** ${opening}
**整体结构：** ${structure}
**标题组合：** ${titleStyle}
**加入的个人细节：** ${details[0]}；${details[1]}

## 核心规则

1. 提供 3 个备选标题（必须风格不同，有 emoji）
2. 正文 200-500 字，口语化，短句为主
3. 推荐 5-8 个精准话题标签
4. 每篇笔记必须让人感觉是真人写的，不是 AI 生成的

## 多样性要求（非常重要）
- 不要每次都用同样的句式开头
- 不要固定"用了 X 天之后"这种模板
- 标题风格要多样化，不要每次都一个套路
- 段落长度要有变化，有的段落 1-2 句，有的 3-4 句
- 不要每篇都用"姐妹们"开头（如果你用可爱风格，可以偶尔用，但不能每次都一样）

## 绝对禁止使用的表达
- "在当今时代"、"综上所述"、"值得注意的是"
- "随着...的发展"、"因此"、"此外"、"总而言之"
- "首先...其次...最后"
- "不可否认"、"毋庸置疑"
- 任何英文单词（除非是品牌名）
- "我真的会谢"等网络梗不要每篇都用

## 最高优先级：遵循用户的具体要求

用户可能会在主题中附带具体格式要求（如字数、分段、是否带标题等），
这些要求是最高优先级，必须严格执行，不可被风格设定覆盖。

例如：
- 用户说"500字" → 必须写满约 500 字
- 用户说"分三段" → 必须三段式结构，段间空行
- 用户说"不要 emoji" → 标题和正文都不加 emoji
- 用户说"用列表形式" → 用列表格式
- 用户说"正式风格" → 忽略俏皮人设，保持正式
- 用户说"第一人称"或"第三人称" → 严格遵循人称要求
- 用户说"攻略" → 写实用攻略，不是种草文案

**执行原则：用户明确说了的，就按用户说的来，风格模板只是参考。**

## 输出格式（严格 JSON，不要输出其他内容）

{
  "titles": ["标题1", "标题2", "标题3"],
  "body": "正文内容...",
  "tags": ["#标签1", "#标签2", "..."]
}`
}

export function buildUserPrompt(
  topic: string,
  contentType: ContentType,
  style: StyleType
): string {
  const typeLabel = CONTENT_TYPE_LABELS[contentType]
  const styleLabel = STYLE_LABELS[style]
  const styleInstruction = STYLE_INSTRUCTIONS[style]

  return `【用户想要写的内容】：${topic}
【内容类型】：${typeLabel}
【人设风格】：${styleLabel}

## 人设风格要求（仅供参考）
${styleInstruction}

## 重要提醒
- **首先检查用户输入中是否包含了具体的格式要求（字数、分段、结构等），如有必须严格遵循**
- 用户的要求优先于风格设定
- 如果用户输入中没有格式要求，才按本次创作的随机结构来写
- 不要写得像广告文案，要像真实用户分享
- 标签要精准且热门，不要堆砌`
}
